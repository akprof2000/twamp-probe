using SPI.Twamp.Probe.Contracts;

namespace SPI.Twamp.Probe.Abstractions
{
    /// <summary>
    /// Диспетчер выполнения зондов: принимает задачи в очередь и раздаёт их
    /// фиксированному пулу рабочих потоков.
    /// <para>
    /// Это ключ к устойчивой работе при ~10 000 задач: сколько бы задач ни
    /// поступило, одновременно выполняется не больше числа воркеров, а очередь
    /// не «заваливает» пул потоков и не тормозит завершение уже запущенных зондов.
    /// </para>
    /// </summary>
    public interface IProbeDispatcher
    {
        /// <summary>
        /// Ставит задачу в очередь на выполнение. Метод неблокирующий:
        /// возвращает управление сразу, реальное выполнение идёт в фоне.
        /// </summary>
        /// <param name="task">Задача для выполнения зонда.</param>
        void Enqueue(TaskInfo task);
    }
}
